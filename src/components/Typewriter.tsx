"use client";
import {useState, useEffect} from "react";
import Cursor from "./Cursor";

interface TypewriterProps{
    text: string;
    speed?: number;
    className?: string;
}

export default function Typewriter({ text, speed = 100, className = ""}: TypewriterProps){
    const [displayedText, setDisplayedText] = useState("");
    const [isFinished, setIsFinished] = useState(false);
    
    useEffect(() => {
        let i = 0;
        setDisplayedText(""); //reinicia al cambiar de texto
        setIsFinished(false); //Marca el inicio de la escritura
        
    const timer = setInterval(() => {
      setDisplayedText(text.slice(0, i + 1));
      i++;
      
      if (i >= text.length) {
        clearInterval(timer);
        setIsFinished(true); //Marca el final de la escritura
      }
    }, speed);
    
    return () => { clearInterval (timer);
    };
    }, [text, speed]);
    
    return(
        <span className = {`font-mono ${className}`}>
            {displayedText}
            <Cursor isAnimating = {!isFinished}/>
        </span>
    );
}
